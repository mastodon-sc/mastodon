package org.mastodon.ui.coloring;

/**
 * Interface for classes that have {@link ColorBarOverlay}.
 */
public interface HasColorBarOverlay
{

	/**
	 * Returns the {@link ColorBarOverlay} of this view.
	 * 
	 * @return the {@link ColorBarOverlay}.
	 */
	public ColorBarOverlay getColorBarOverlay();

}
